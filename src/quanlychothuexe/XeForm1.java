public void loadData(){
        try{
            
             Connect a=new Connect();
            Connection conn = a.getConnection() ;
            int number;
            Vector row,column;
            column = new Vector();
             Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("Select * from tbXe");
            ResultSetMetaData metadata = rs.getMetaData();
            number = metadata.getColumnCount();
            for(int i=1;i<=number;i++){
                column.add(metadata.getColumnName(i));
            }
            tbn.setColumnIdentifiers(column);
            while(rs.next()){
                row = new Vector();
                for(int i=1;i<=number;i++){
                    row.addElement(rs.getString(i));
                }
                tbn.addRow(row);
                tbxe.setModel(tbn);
            }
            // day du lieu tu table len field
            tbxe.getSelectionModel().addListSelectionListener(new ListSelectionListener(){
               @Override
               public void valueChanged(ListSelectionEvent e) {
                   if(tbxe.getSelectedRow()>=0){
                       txtMaXe.setText(tbxe.getValueAt(tbxe.getSelectedRow(), 0)+"");
                       txtTenXe.setText(tbxe.getValueAt(tbxe.getSelectedRow(), 1)+"");
                        txtMauXe.setText(tbxe.getValueAt(tbxe.getSelectedRow(), 2)+"");
                         txtBienSo.setText(tbxe.getValueAt(tbxe.getSelectedRow(), 3)+"");
                         txtMaLoaiXe.setText(tbxe.getValueAt(tbxe.getSelectedRow(), 4)+"");
                           txtSoCho.setText(tbxe.getValueAt(tbxe.getSelectedRow(), 5)+"");
                         txtfmGiaXe.setText(tbxe.getValueAt(tbxe.getSelectedRow(), 6)+"");
                          txtTrangThai.setText(tbxe.getValueAt(tbxe.getSelectedRow(), 7)+"");
                           txtmahd.setText(tbxe.getValueAt(tbxe.getSelectedRow(), 8)+"");
                        
                   }
               }
                
            });
        }
        catch(Exception ex){
            System.out.println(ex.toString());
        }
}
