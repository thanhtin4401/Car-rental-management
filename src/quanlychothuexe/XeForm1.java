    private void btn_ThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ThemActionPerformed
        try{
            Connect a = new Connect();
            Connection conn = a.getConnection() ;
            PreparedStatement ps = conn.prepareStatement("Insert into tbXe values (?,?,?,?,?,?,?,?,?)");
            ps.setString(1,txtMaXe.getText());
            ps.setString(2,txtTenXe.getText());
            ps.setString(3,txtMauXe.getText());
            ps.setString(4,txtBienSo.getText());
            ps.setString(5,txtMaLoaiXe.getText());
            ps.setString(6,txtSoCho.getText());
            ps.setString(7,txtfmGiaXe.getText());
            ps.setString(8,txtTrangThai.getText());
            ps.setString(9,txtmahd.getText());
            int check = ps.executeUpdate();
            if(check>0){
                JOptionPane.showMessageDialog(this, "Thêm thành công");
                tbn.setRowCount(0);//truoc khi load thì set so cot ve 0 neu khong se load du lieu len bang 1 lan nua
                loadData();
            }else{
                JOptionPane.showMessageDialog(this, "Không hợp lệ, vui lòng nhập lại");
            }
        }catch(Exception ex){
            System.out.println(ex.toString());
        }
    }//GEN-LAST:event_btn_ThemActionPerformed

